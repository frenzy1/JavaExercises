public class HelloWorld 
{
	public static void main (String[] args)
	{
		double a;
		double b;

		double sum;

		a = 1;
		b = 2;

		sum = a + b;

		System.out.println("Hello World!");		
		System.out.println("a + b = " + sum);

		sum = a - b;

		System.out.println("a - b = " + sum);
		sum = a * b;

		System.out.println("a * b = " + sum);
		sum = a / b;

		System.out.println("a / b = " + sum);
		sum = a % b;

		System.out.println("a % b = " + sum);
	}
}
